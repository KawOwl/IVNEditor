// 主流程（mimic seed-m3-cafe）
// ============================================================================
async function main() {
  // 1. 找 admin 用户
  const adminRows = await db
    .select({ id: schema.users.id, username: schema.users.username })
    .from(schema.users)
    .where(eq(schema.users.roleId, 'admin'))
    .limit(1);

  if (adminRows.length === 0) {
    console.error('[seed-anjie] 找不到 admin 用户，先跑 seed-admin.ts');
    process.exit(1);
  }
  const admin = adminRows[0]!;
  console.log(`[seed-anjie] author = ${admin.username} (${admin.id})`);

  // 2. upsert script 身份
  const manifest = buildManifest();
  const script = await scriptService.create({
    id: manifest.id,
    authorUserId: admin.id,
    label: manifest.label,
    description: manifest.description,
  });
  console.log(`[seed-anjie] script upsert: ${script.id} "${script.label}"`);

  // 3. 发布新版本
  const result = await scriptVersionService.create({
    scriptId: script.id,
    manifest,
    status: 'published',
    label: '暗街Seed3',
    note: 'Seed3：对照旧版剧情数据库补回 17 处（A–Q）细节，让角色动机/选项契约在 prompt 里齐全。\n  A 骷髅会委托来源（玩家拍摄委托来自骷髅会）\n  B 卡尔复活/复仇/棋子双重性（祂曾死于黑手党、向新西西里复仇、卡琳娜既是棋子也是唯一同伴）\n  C 帕兹 5 条动机钳制（罪孽/心结/愧疚/离开/底线）+ 模糊输入 fallback\n  D 卡琳娜 3 侧面（恶劣玩笑/不狂热/"尊严最贵重"）\n  E 康纳行为开关（与卡琳娜相关话题外决不放手）\n  F 康纳三重固定印象（妹妹/墓碑/拒绝改变）\n  G 康纳权力欲根源（暗街出身的屈辱与不甘）\n  H 阶段一退出信号 4 具体化（卡琳娜以"嫌麻烦"锚点）\n  I 阶段三复仇主题显化（卡尔讽刺德索洛+断言 "复仇"+卡琳娜要让卡尔活在阳光下）\n  J 阶段五三条信息暗线（怪物传闻/叛徒情报/凯旋门掩护暗街）\n  K 阶段六结局必备画面（共犯→铃铛+"不属于这里"+机票取消+为她拍照；分道扬镳→飞机窗外火海；新 state var has_bell）\n  L 康纳线·一三条秘密（凯旋门并非铁板一块/卡琳娜一笔账/禁足令）\n  M 康纳线·二进阶分支（已解锁 ≥2 秘密→请柬→接受赴宴 or 拒绝→白夜线触发）\n  N 阶段五康纳赞赏回响（"学得很快"+卡琳娜降温）\n  O 白夜线分支 D "参与复仇"（见证人路径）+ C1 带走失败回收（卡琳娜把活泼好奇面交给玩家）\n  P 白夜线"今晚点燃新西西里"迫切性（床下罐子/立绘切换 curious→vulnerable→curious）\n  Q 启动阶段三条硬规则（姓名仅询问一次/模糊输入 fallback 帕兹/选项严格遵守设定）\n继承 Seed2：第一章 6 种结局（共犯/记录者/分道扬镳/白夜真/普通白夜/卡尔线）、3 个支线场景（康纳办公室/港口仓库/卡琳娜真实住所）、state 变量（side_route/asked_past/ch1_ending）、ch1 flowGraph。白夜真结局仍是 ch2 白夜开篇的前置。',
  });
  if (result.created) {
    console.log(`[seed-anjie] 新版本 v${result.version.versionNumber} published (id=${result.version.id})`);
  } else {
    console.log(`[seed-anjie] 内容 hash 未变，复用 v${result.version.versionNumber} (status=${result.version.status})`);
  }

  // 4. 段落统计
  const chCount = manifest.chapters.length;
  const segCount = manifest.chapters.reduce((n, c) => n + c.segments.length, 0);
  const tokenTotal = manifest.chapters.reduce(
    (n, c) => n + c.segments.reduce((m, s) => m + s.tokenCount, 0), 0,
  );
  console.log(`[seed-anjie] chapters=${chCount} segments=${segCount} tokens~${tokenTotal}`);

  console.log('[seed-anjie] 完成。编辑器首页应能看到《暗街》卡片。');
  process.exit(0);
}

main().catch((err) => {
  console.error('[seed-anjie] 致命错误:', err);
  process.exit(1);
});
